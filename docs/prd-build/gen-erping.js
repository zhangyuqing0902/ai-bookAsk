function runs(text, opt = {}) {
  const parts = String(text).split(/(\*\*[^*]+\*\*)/g).filter(Boolean);
  return parts.map((p) => {
    const b = /^\*\*[^*]+\*\*$/.test(p);
    return new TextRun({ text: b ? p.slice(2, -2) : p, bold: b || opt.bold, color: opt.color || INK, size: opt.size || 21, font: FONT });
  });
}
const P = (text, opt = {}) => new Paragraph({ spacing: { after: opt.after ?? 120, line: 300 }, alignment: opt.align, children: runs(text, opt) });
const H1 = (text) => new Paragraph({ heading: HeadingLevel.HEADING_1, spacing: { before: 300, after: 150 }, children: [new TextRun({ text, bold: true, size: 30, color: INDIGO, font: FONT })] });
const H2 = (text) => new Paragraph({ spacing: { before: 200, after: 90 }, children: [new TextRun({ text, bold: true, size: 24, color: INK, font: FONT })] });
// 新增 / 调整 / 删除 子标题（带色点）
const SUB = (text, color) => new Paragraph({ spacing: { before: 120, after: 60 }, children: [new TextRun({ text: '● ', color, size: 20, font: FONT }), new TextRun({ text, bold: true, size: 21, color: INK, font: FONT })] });
const BULLET = (text) => new Paragraph({ numbering: { reference: 'b', level: 0 }, spacing: { after: 70, line: 288 }, children: runs(text) });

function cell(text, { w, head } = {}) {
  return new TableCell({
    borders, width: { size: w, type: WidthType.DXA }, margins: cellMargins, verticalAlign: VerticalAlign.CENTER,
    shading: head ? { fill: 'E9ECF7', type: ShadingType.CLEAR } : undefined,
    children: [new Paragraph({ spacing: { after: 0, line: 278 }, alignment: head ? AlignmentType.CENTER : undefined, children: runs(text, { bold: head, size: head ? 20 : 19 }) })],
  });
}
function table(widths, header, rows) {
  return new Table({
    width: { size: CONTENT, type: WidthType.DXA }, columnWidths: widths,
    rows: [
      new TableRow({ tableHeader: true, children: header.map((h, i) => cell(h, { w: widths[i], head: true })) }),
      ...rows.map((r) => new TableRow({ children: r.map((c, i) => cell(c, { w: widths[i] })) })),
    ],
  });
}
const ADD = '0F7A59', CHG = '4B57E8', DEL = 'C2410C';
const sec = (title, add, chg, del) => {
  const out = [H1(title)];
  out.push(SUB('新增', ADD)); add.forEach((t) => out.push(BULLET(t)));
  out.push(SUB('调整 / 修复', CHG)); chg.forEach((t) => out.push(BULLET(t)));
  out.push(SUB('删除', DEL)); del.forEach((t) => out.push(BULLET(t)));
  return out;
};

