// 功能点块：名称（靛蓝小三角引导）+ 描述 + 规则要点
function FEAT(name, desc, rules) {
  const out = [new Paragraph({ spacing: { before: 170, after: 60, line: 300 }, children: [new TextRun({ text: '▍ ' + name, bold: true, color: CLR.accent, size: 22, font: CJK })] })];
  if (desc) out.push(P(desc, { after: rules && rules.length ? 70 : 130 }));
  if (rules && rules.length) {
    out.push(new Paragraph({ spacing: { after: 30, line: 280 }, children: [new TextRun({ text: '规则与交互要点', bold: true, color: CLR.sub, size: 18, font: CJK })] }));
    out.push(...rules.map((it) => new Paragraph({ numbering: { reference: 'bul', level: 0 }, spacing: { after: 50, line: 292 }, children: runs(it) })));
  }
  return out;
}

// ---------------- 表格 ----------------
function TBL(headers, rows, widths, opt = {}) {
  const total = widths.reduce((a, b) => a + b, 0);
  const bd = { style: BorderStyle.SINGLE, size: 1, color: CLR.line };
  const borders = { top: bd, bottom: bd, left: bd, right: bd };
  const cellPar = (c, hdr) => (Array.isArray(c) ? c : [c]).map((line, idx) =>
    new Paragraph({ spacing: { line: 270, after: idx === (Array.isArray(c) ? c.length - 1 : 0) ? 0 : 40 }, children: runs(String(line), hdr ? { bold: true, color: CLR.th, size: 18 } : { size: opt.size || 18 }) }));
  const head = new TableRow({ tableHeader: true, children: headers.map((h, i) => new TableCell({ borders, verticalAlign: VerticalAlign.CENTER, width: { size: widths[i], type: WidthType.DXA }, shading: { fill: CLR.soft, type: ShadingType.CLEAR }, margins: { top: 70, bottom: 70, left: 120, right: 120 }, children: cellPar(h, true) })) });
  const body = rows.map((r, ri) => new TableRow({ children: r.map((c, i) => new TableCell({ borders, verticalAlign: VerticalAlign.CENTER, width: { size: widths[i], type: WidthType.DXA }, shading: ri % 2 === 1 ? { fill: CLR.zebra, type: ShadingType.CLEAR } : undefined, margins: { top: 60, bottom: 60, left: 120, right: 120 }, children: cellPar(c) })) }));
  return new Table({ width: { size: total, type: WidthType.DXA }, columnWidths: widths, rows: [head, ...body] });
}

