// ---------------- 行内 **加粗** / `代码` 解析 ----------------
function runs(text, base = {}) {
  if (Array.isArray(text)) return text;
  const parts = String(text).split(/(\*\*[^*]+\*\*|`[^`]+`)/g).filter((s) => s !== '');
  return parts.map((p) => {
    if (p.startsWith('**') && p.endsWith('**')) return new TextRun({ text: p.slice(2, -2), bold: true, font: CJK, ...base });
    if (p.startsWith('`') && p.endsWith('`')) return new TextRun({ text: p.slice(1, -1), font: { ascii: 'DM Mono', eastAsia: 'PingFang SC', hAnsi: 'DM Mono' }, color: CLR.accent, ...base });
    return new TextRun({ text: p, font: CJK, ...base });
  });
}

// ---------------- 段落 / 列表 / 标题 ----------------
const P = (t, o = {}) => new Paragraph({ spacing: { after: o.after ?? 120, line: o.line ?? 312, before: o.before ?? 0 }, alignment: o.align, children: runs(t, o.base || {}) });
const LEAD = (label, t) => new Paragraph({ spacing: { after: 80, line: 312, before: 60 }, children: [new TextRun({ text: label + '　', bold: true, color: CLR.accent, font: CJK }), ...runs(t)] });
const BUL = (items) => items.map((it) => new Paragraph({ numbering: { reference: 'bul', level: 0 }, spacing: { after: 60, line: 300 }, children: runs(it) }));
